/**
 * Russian has three plural forms — "one" (1, 21, 31…), "few" (2–4, 22–24…) and
 * "many" (0, 5–20, 25–30…, incl. the 11–14 exception). `Intl.PluralRules`
 * implements the CLDR rule, so the exceptions are handled for us.
 */
const ruPluralRules = new Intl.PluralRules("ru");
function plural(count: number, one: string, few: string, many: string): string {
  const form = ruPluralRules.select(count);
  return form === "one" ? one : form === "few" ? few : many;
}

